# Playing around with For Loops


for letter in "Giridhar Shenoy":
    print(letter)

# The output for above will be
# G
# i
# r
# i
# d
# h
# a
# r

# S
# h
# e
# n
# o
# y


friends = ['raja', 'ram', 'mohan', 'roy']

for friend in friends:
    print(friend)

# Output for above will be
# raja
# ram
# mohan
# roy


for friend in range(len(friends)):
    print(friends[friend])

# Output for above will be
# raja
# ram
# mohan
# roy


for index in range(10):
    print(index)   # Will print 0-9 vertically


for index in range(3, 10):
    print(index)  # Will spit out numbers from 3 to 9


